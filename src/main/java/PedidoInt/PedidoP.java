package PedidoInt;

public class PedidoP {

    private String nome_cliente, rua, bairro, numero_casa, referencia, tamanho_pixza, sabor_pixza, bebidas, valor_pedido;

    public String getNome_cliente() {
        return nome_cliente;
    }

    public void setNome_cliente(String nome_cliente) {
        this.nome_cliente = nome_cliente;
    }

    public String getRua() {
        return rua;
    }

    public void setRua(String rua) {
        this.rua = rua;
    }

    public String getBairro() {
        return bairro;
    }

    public void setBairro(String bairro) {
        this.bairro = bairro;
    }

    public String getNumero_casa() {
        return numero_casa;
    }

    public void setNumero_casa(String numero_casa) {
        this.numero_casa = numero_casa;
    }

    public String getReferencia() {
        return referencia;
    }

    public void setReferencia(String referencia) {
        this.referencia = referencia;
    }

    public String getTamanho_pixza() {
        return tamanho_pixza;
    }

    public void setTamanho_pixza(String tamanho_pixza) {
        this.tamanho_pixza = tamanho_pixza;
    }

    public String getSabor_pixza() {
        return sabor_pixza;
    }

    public void setSabor_pixza(String sabor_pixza) {
        this.sabor_pixza = sabor_pixza;
    }

    public String getBebidas() {
        return bebidas;
    }

    public void setBebidas(String bebidas) {
        this.bebidas = bebidas;
    }

    public String getValor_pedido() {
        return valor_pedido;
    }

    public void setValor_pedido(String valor_pedido) {
        this.valor_pedido = valor_pedido;
    }

}
